// Load music list dynamically
const musicList = document.getElementById("music-list");

function loadMusic(data) {
musicList.innerHTML = ""; // Clear the existing music list  
data.forEach((track) => {
    const card = document.createElement("div");
    card.className = "card";

    // Create a unique background video for each music track  
    // Removed the video section and only included title and artist  
    card.innerHTML = `  
        <h3>${track.title}</h3>  
        <p>${track.artist} (${track.language})</p>  
        <audio controls>  
            <source src="${track.src}" type="audio/mpeg">  
            Your browser does not support the audio element.  
        </audio>   
    `;

    musicList.appendChild(card);
});
}

// Search functionality  
const searchBar = document.getElementById("search-bar");
searchBar.addEventListener("input", (e) => {
const query = e.target.value.toLowerCase();
const filteredMusic = musicData.filter((track) =>
    track.title.toLowerCase().includes(query) ||
    track.artist.toLowerCase().includes(query) ||
    track.language.toLowerCase().includes(query)
);
loadMusic(filteredMusic); // Load the filtered music  
});